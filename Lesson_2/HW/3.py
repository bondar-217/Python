def print_powers_of_two(N):
    power = 0  # Начальная степень двойки
    result = 1  # Первое значение степени двойки, 2^0 = 1

    while result <= N:  # Пока значение степени двойки не превышает N
        print(result, end=" ")  # Выводим текущее значение степени двойки
        power += 1  # Увеличиваем значение степени на 1
        result = 2 ** power  # Вычисляем следующее значение степени двойки

# Пример использования:
N = 20
print_powers_of_two(N)
