def sum(a, b):
    # Базовый случай: если одно из чисел равно 0, возвращаем другое число
    if a == 0:
        return b
    if b == 0:
        return a

    # Рекурсивный случай: меняем знак у одного из чисел и увеличиваем/уменьшаем другое число на 1
    if a > 0:
        return sum(a - 1, b + 1)
    else:
        return sum(a + 1, b - 1)


# Ввод чисел a и b
a = int(input("Введите первое число a: "))
b = int(input("Введите второе число b: "))

# Вызов функции sum и вывод результата
result = sum(a, b)
print("Сумма чисел", a, "и", b, ":", result)
